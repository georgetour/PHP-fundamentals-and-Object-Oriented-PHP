        <h2>What back end means?</h2>
        <p>In simple words :</p>
        <p> When we built a site we have front end or client side with HTML, CSS and javascript
            which is what the user sees in the browser.
        </p>
        <p> At back end or server-side is stuff that the server runs and aren't accessible or visible directly to the user
            . When you complete a form, sign in ,passwords etc. then a back end language will take charge like PHP.
            Imagine it like the behind the scenes of a theatrical play. Wouldn't be awkward seeing actors change?
            The same applies for back end. Wouldn't be wrong if you could see stored passwords, databases and some details
            that shouldn't the user see in the browser.
        </p>
